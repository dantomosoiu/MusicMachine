using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

/// <summary>
/// Se foloseste la editarea profilului utilizatorului curent (pentru completarea campurilor text).
/// </summary>
public class ExtendedPersonalInfo: PersonalInfo
{
    public string MySkin;

	public ExtendedPersonalInfo(string FirstName, string LastName, string Email, string Sex,
        int Age, string Location, List<string> FavouriteArtists, List<string> FavouriteGenres, string MySkin): 
        base(FirstName, LastName, Email, Sex, Age, Location, FavouriteArtists, FavouriteGenres)
	{
        this.MySkin = MySkin;
	}
}